function buildRateOfReturn(doc, inputs, results) {
  const payoutLabel = results.isPaidOut ? `Payout: Month ${results.payoutMonth}` : "Not recovered in timeframe";
  let y = addHeader(doc, CALC_TITLES.rate_of_return, payoutLabel);
  
  y = addSectionTitle(doc, y, "INVESTMENT PARAMETERS");
  y = addRow(doc, y, "Net Investment Amount", fmt(inputs.netInvestment));
  y = addRow(doc, y, "Monthly Oil Income", fmt(inputs.monthlyOilIncome));
  y = addRow(doc, y, "Monthly Gas Income", fmt(inputs.monthlyGasIncome));
  y = addRow(doc, y, "Combined Monthly Income", fmt(inputs.monthlyOilIncome + inputs.monthlyGasIncome));
  y = addRow(doc, y, "Time Horizon", inputs.timeHorizon + " months");
  y = addRow(doc, y, "Annual Production Decline", fmtPct(inputs.annualDeclineRate * 100));
  
  y += 4;
  y = addSectionTitle(doc, y, "RETURN ANALYSIS");
  y = addRow(doc, y, "Total Revenue over Period", fmt(results.totalRevenue), true);
  y = addRow(doc, y, "Simple ROI", fmtPct(results.simpleROI));
  y = addRow(doc, y, "Internal Rate of Return (Annual)", results.annualIRR !== null ? fmtPct(results.annualIRR) : "N/A", true);
  y = addRow(doc, y, "Monthly Cash-on-Cash Yield", fmtPct(results.monthlyCashOnCash));
  y = addRow(doc, y, "Payout Period", results.isPaidOut ? `Month ${results.payoutMonth}` : "Not within timeframe");
  y = addRow(doc, y, "Simple Annualized Return", fmtPct(results.annualizedReturn));
  
  y += 6;
  doc.setFillColor(46, 125, 50, results.isPaidOut ? 15 : 0);
  if (!results.isPaidOut) doc.setFillColor(212, 168, 67, 20);
  doc.rect(12, y - 3, 186, 12, 'F');
  doc.setFontSize(10);
  doc.setTextColor(results.isPaidOut ? 46 : 180, results.isPaidOut ? 125 : 120, results.isPaidOut ? 50 : 0);
  doc.text(
    results.isPaidOut 
      ? `Investment pays out in Month ${results.payoutMonth} (${(results.payoutMonth / 12).toFixed(1)} years)`
      : "Investment does not pay out within the selected time horizon",
    105, y + 4, { align: 'center' }
  );
  
  addFooter(doc);
}

Deno.serve(async (req) => {
  try {
    const base44 = createClientFromRequest(req);
    const user = await base44.auth.me();
    
    if (!user) {
      return Response.json({ error: 'Unauthorized' }, { status: 401 });
    }

    const { calcType, inputs, results } = await req.json();
    
    if (!calcType || !inputs || !results) {
      return Response.json({ error: 'Missing calcType, inputs, or results' }, { status: 400 });
    }

    const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
    
    switch (calcType) {
      case 'net_investment':
        buildNetInvestment(doc, inputs, results);
        break;
      case 'barrels_to_cash':
        buildBarrelsToCash(doc, inputs, results);
        break;
      case 'natgas_to_cash':
        buildNatGasToCash(doc, inputs, results);
        break;
      case 'rate_of_return':
        buildRateOfReturn(doc, inputs, results);
        break;
      default:
        return Response.json({ error: 'Unknown calculator type' }, { status: 400 });
    }

    const pdfBytes = doc.output('arraybuffer');

    return new Response(pdfBytes, {
      status: 200,
      headers: {
        'Content-Type': 'application/pdf',
        'Content-Disposition': `attachment; filename=EnergyCalc_${calcType}_${new Date().toISOString().split('T')[0]}.pdf`
      }
    });
  } catch (error) {
    console.error('PDF generation error:', error);
    return Response.json({ error: "Internal server error" }, { status: 500 });
  }
});